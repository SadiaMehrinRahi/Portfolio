export function iconFor(label: string) {
  const l = label.toLowerCase();
  if (l.includes("github")) return <GitHubIcon />;
  if (l.includes("linkedin")) return <LinkedInIcon />;
  if (l.includes("researchgate")) return <ResearchGateIcon />;
  if (l.includes("scholar")) return <ScholarIcon />;
  if (l.includes("blog")) return <BlogIcon />;
  if (l.includes("mail") || l.includes("email")) return <MailIcon />;
  return null;
}
